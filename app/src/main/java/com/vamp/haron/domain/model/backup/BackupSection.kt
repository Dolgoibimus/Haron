package com.vamp.haron.domain.model.backup

/**
 * Секции бэкапа — пользователь выбирает какие данные включить в бэкап.
 *
 * Каждая секция соответствует определённому типу данных:
 * - [SETTINGS] — все SharedPreferences (настройки UI, жесты, теги, избранное, закладки и т.д.)
 * - [CREDENTIALS] — сетевые учётки (FTP/SMB/SSH/WebDAV) + облачные токены (Yandex/GDrive/Dropbox).
 *   Учётки хранятся зашифрованными в Android Keystore (AES-256-GCM), ключ привязан к чипу
 *   безопасности устройства и не может быть извлечён. При бэкапе учётки расшифровываются
 *   на текущем устройстве и кладутся в ZIP, защищённый паролем пользователя.
 *   При восстановлении на новом устройстве — шифруются новым ключом Keystore.
 * - [BOOKS] — библиотека книг (Room DB: таблицы book + reading_position) и позиции видео.
 * - [SECURE_FOLDER] — файлы защищённой папки. Аналогично credentials — расшифровываются
 *   при бэкапе, т.к. ключ Keystore непереносим между устройствами.
 */
enum class BackupSection {
    SETTINGS,
    CREDENTIALS,
    BOOKS,
    SECURE_FOLDER
}
