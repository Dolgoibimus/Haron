    /** Case-insensitive lookup: returns RU translation or the original label */
    fun translate(label: String): String {
        return dictionary[label]
            ?: dictionary.entries.firstOrNull { it.key.equals(label, ignoreCase = true) }?.value
            ?: label
    }

    /**
     * Builds a searchable string with both EN and RU terms:
     * "Dog Собака Cat Кошка Car Автомобиль"
     */
    fun buildSearchableText(labels: List<String>): String {
        return labels.joinToString(" ") { label ->
            val ru = translate(label)
            if (ru != label) "$label $ru" else label
        }
    }
